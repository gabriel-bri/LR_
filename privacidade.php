Como coletamos informações
Nosso principal objetivo ao coletar informações
pessoais é levar até você o melhor conteúdo. Existem
basicamente duas formas de coletarmos informações
sobre nossos leitores: por meio das interações com o
site e pelos dados fornecidos voluntariamente.
Na primeira situação, obtemos e arquivamos estatís‐
ticas sobre as atividades de nossos leitores, como,
por exemplo, quais são as páginas mais visitadas e o
tempo gasto em cada seção. Na segunda, obtemos
informações fornecidas mediante cadastramento,
pesquisas, promoções e outros, sempre com o seu
consentimento.
Como usamos esses dados
Utilizamos as informações genéricas que obtemos
para continuamente melhorar nosso site, veicular
publicidade e oferecer alternativas de comércio. Além
disso, usamos os dados (tanto genéricos quanto
pessoais) para melhorar sua experiência, oferecendo
conteúdo, propaganda e produtos que acreditamosser relevantes para você.
Utilizamos seu e-mail para enviar produtos do site
(como nosso boletim semanal) ou para divulgar
promoções e concursos. Nosso objetivo é retribuir a
sua atenção ao encaminhar ofertas diferenciadas com
benefícios exclusivos.
Os dados que compartilhamos com terceiros são
apenas genéricos e agregados, provenientes das
interações com os sites. Por exemplo, podemos dizer
a um anunciante que um número de pessoas visitou
determinada seção ou se cadastrou em nossos sites.
Por outro lado, não divulgamos nenhuma informação
que possa identificar essas pessoas, ou seja, nunca
divulgamos seu e-mail, nome ou qualquer outro dado
que possa identificá-lo. Terceiros também poderão
coletar dados sobre navegação, mas você pode optar
por sair neste endereço: http://optout.t.tailtarget.com.
Cookies
Cookies são pequenos arquivos alocados em seu
computador. Eles permitem que sua senha seja
guardada, evitando que você tenha de redigitá-la
diversas vezes. A partir deles, podemos entender
melhor como os leitores interagem com os sites,
oferecendo conteúdos, publicidade e produtos que
correspondam a seus interesses e necessidades.
Nós também usamos cookies para evitar fraudes no
site e levar até você informações precisas da seguinte
forma:
- Quando você baixa um programa ou vota em uma
enquete nós gravamos um cookie no seu computador
para saber que você já baixou aquele programa ou já
votou naquela enquete, evitando assim que pessoas
mal intencionadas votem diversas vezes na mesma
enquete ou alterem deliberadamente o número de
downloads de um programa.
Você pode optar por não aceitar nossos cookies
(mediante configuração de seu navegador), mas
algumas áreas de nosso site podem não funcionar
corretamente.
Usamos a rede Google Adsense para veicular anúncios
durante a sua visita ao nosso website. O Google
pode usar informações (que não incluem o seu nome,
endereço, endereço de e-mail ou número de telefone)
sobre suas visitas a este e a outros websites a fim de
exibir anúncios relacionados a produtos e serviços
de seu interesse. Para obter mais informações sobre
essa prática e saber como impedir que o Google utilize
esses dados, clique aqui.
Segurança 
